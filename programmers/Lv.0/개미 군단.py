# Claude가 알려준 최종 정답 통과한 코드
def solution(hp):
    answer = 0
    min_sum = float('inf') 
    # float('inf')는 '무한대'를 의미하고, 
    # 이걸 사용해서 가장 큰 값부터 시작해서 작으면 current_sum(= a + b + c) 값이 이 min_sum 보다 작으면 그 작은 값을 덮어쓰면서 최솟값을 찾아감.
    
    for a in range(0, hp // 5 + 1):  # a의 최댓값(b와 c가 0이면 n=3a, a=n/3)
        for b in range(0, (hp - 5*a) // 3 + 1):  # b의 최대값(a가 정해진 상태이므로 b의 최댓값 = c가 0일 때!)
            c = hp - 5*a - 3*b
            if c >= 0:  
                current_sum = a + b + c
                if current_sum < min_sum:
                    min_sum = current_sum
                    answer = current_sum
    return answer
    
