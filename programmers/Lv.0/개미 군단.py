# 개미 군단은 사냥감의 체력 hp에 개미 군단의 공격력을 맞춰서 사냥을 나가야 하는데, 최소한의 병력으로 개미 군단을 구성해야 함.
# 예를 들어 사냥감의 체력이 23이라면 일개미 23마리를 데리고 가면 안되고, 장군개미 4마리와 병정개미 1마리로 사냥에 나갸야 함.
# hp가 매개변수로 주어질 때, 최소한의 병력을 구성할 수 있도록 함수를 완성하는 문제

# 최소한의 병력..
# 그니까 구해야 되는데 5a + 3b + c = hp가 되는 (a, b, c)의 순서쌍 중에
# a + b + c 값이 최소가 되는 순서쌍을 찾아야 함. 이걸 코드로 구현하면?
# for문으로 (a, b, c) 쌍을 돌리는데, 이 for문 내부에서 if문을 사용해서 저 3개의 변수의 합이 최소일 때를 그 합을 반환하는 함수.

# hp = 5a + 3b + c인 random 자연수쌍 (a, b, c)를 돌면서
# a + b + c 값이 최소가 되는 (a, b, c)를 출력하는 반복문
